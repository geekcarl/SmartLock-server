package com.sxt.po;

public class CoreAtomType {

	private int op_id;   //光缆id
	private String op_name;   //光缆名称
	private int  core_id;  //纤芯id
	private  String  core_name;   //纤芯name
	private int sequence;  //纤芯序列号
	
	
	public int getOp_id() {
		return op_id;
	}
	public void setOp_id(int op_id) {
		this.op_id = op_id;
	}
	public String getOp_name() {
		return op_name;
	}
	public void setOp_name(String op_name) {
		this.op_name = op_name;
	}
	public int getCore_id() {
		return core_id;
	}
	public void setCore_id(int core_id) {
		this.core_id = core_id;
	}
	public String getCore_name() {
		return core_name;
	}
	public void setCore_name(String core_name) {
		this.core_name = core_name;
	}
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	
	
	
	
	
	

}
