package com.sxt.po;

public class AtomType {

	private String sideName;
	private int row;
	private int  col;
	private int teminal_id;
	
	public String getSideName() {
		return sideName;
	}
	public void setSideName(String sideName) {
		this.sideName = sideName;
	}
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public int getCol() {
		return col;
	}
	public void setCol(int col) {
		this.col = col;
	}
	public int getTeminal_id() {
		return teminal_id;
	}
	public void setTeminal_id(int teminal_id) {
		this.teminal_id = teminal_id;
	}
	
	
	
	

}
